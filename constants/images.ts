  // Helper function to get image URL with optional size parameters
  export const getImageUrl = (
    imageUrl: string, 
    options?: { width?: number; height?: number; quality?: number }
  ) => {
    if (!imageUrl) return "";
    
    // If it's already a full URL, return it
    if (imageUrl.startsWith('http')) {
      // For Unsplash URLs, we can add parameters
      if (imageUrl.includes('unsplash.com') && options) {
        const url = new URL(imageUrl);
        
        // Remove existing width, height, and quality parameters
        const searchParams = url.searchParams;
        if (options.width) searchParams.set('w', options.width.toString());
        if (options.height) searchParams.set('h', options.height.toString());
        if (options.quality) searchParams.set('q', options.quality.toString());
        
        return url.toString();
      }
      return imageUrl;
    }
    
    // If it's a local reference (for future use)
    return imageUrl;
  };